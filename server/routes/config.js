//----------------------------------------------------------------------------------------------------------------------
// Routes for config activity
//
// @module page.js
//----------------------------------------------------------------------------------------------------------------------

var _ = require('lodash');
var express = require('express');

var routeUtils = require('./utils');
var config = require('../config');
var package = require('../../package');

var logger = require('omega-logger').loggerFor(module);

//----------------------------------------------------------------------------------------------------------------------

var router = express.Router();

//----------------------------------------------------------------------------------------------------------------------
// Middleware
//----------------------------------------------------------------------------------------------------------------------

// Basic request logging
router.use(routeUtils.requestLogger(logger));

// Basic error logging
router.use(routeUtils.errorLogger(logger));

//----------------------------------------------------------------------------------------------------------------------
// Config Endpoint
//----------------------------------------------------------------------------------------------------------------------

router.get('/', function(req, resp)
{
    var exposedConfig = _.omit(config, ['sid', 'secret', 'googleSecret']);
    exposedConfig.version = package.version;

    resp.json(exposedConfig);
});

//----------------------------------------------------------------------------------------------------------------------

module.exports = router;

